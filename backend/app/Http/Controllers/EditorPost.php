     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = Auth::user();

        $data = array();

        $lap = lapp::where('user_id', '=', $user->id)->orderBy('id', 'DESC')->get();

        foreach ($lap as $key) 
        {
            $post = Coaching::where('id', '=', $key->coaching_id)->get();

            array_push($data, $post);
        }

        return view('editor.listing.index', compact('data'));
    }

